def calcular_metade_numero(numero):

    valor = float(numero)
    metade = valor / 2
    return metade

def calcular_produto_numero(valor):

    numero = float(valor)
    produto = numero * 2
    return produto

def calcular_aumento_10(preco):

    valor = float(preco)
    aumento = (1 + 0.1) * valor
    return aumento
